void StandardMouseTheme::AddAlphaChannel(Lepra::Canvas* pImage)
{
	// Assuming pImage is 8 bit.

	Lepra::Canvas lAlpha(pImage->GetWidth(), pImage->GetHeight(), Lepra::Canvas::BITDEPTH_8_BIT);
	lAlpha.CreateBuffer();

	unsigned char* lBuffer = (unsigned char*)pImage->GetBuffer();
	unsigned char* lAlphaBuffer = (unsigned char*)lAlpha.GetBuffer();

	for (unsigned int y = 0; y < pImage->GetHeight(); y++)
	{
		unsigned int lSrcOffset = y * pImage->GetPitch();
		unsigned int lDstOffset = y * lAlpha.GetPitch();

		for (unsigned int x = 0; x < pImage->GetWidth(); x++)
		{
			if (lBuffer[lSrcOffset + x] == 0)
			{
				lAlphaBuffer[lDstOffset + x] = 0;
			}
			else
			{
				lAlphaBuffer[lDstOffset + x] = 255;
			}
		}
	}

	pImage->ConvertTo32BitWithAlpha(lAlpha);
}

